#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*s;
	int		i;

	i = 0;
	while (str[i])
		i++;
	s = malloc(sizeof(char) * i + 1);
	if (!s)
		return (NULL);
	i = 0;
	while (str[i])
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
