#include "ft_printf.h"

void	ft_putui_i(unsigned int nb, int *i)
{
	if (nb >= 10)
	{
		ft_putui_i(nb / 10, i);
		nb %= 10;
	}
	ft_putchar_i(nb + '0', i);
}
